# classify_flow
# ============================================================

def classify_flow(timestamps, session_gap=5.0, min_pkts=MIN_INTERVALS,
                  connection_level=False):
    """
    Full two-gate pipeline for bounded quasi-random beacons.

    Classification hierarchy:
      ROTATION_BEACON  -- Both gates pass (irrational rotation schedule)
      REGULAR_BEACON   -- Constant-interval periodic
      JITTERED_BEACON  -- Bounded low-CV periodic
      BACKGROUND       -- No detectable structure
      INSUFFICIENT_DATA
    """
    ts = sorted(timestamps)
    n = len(ts)

    if n < min_pkts + 1:
        return {"classification": "INSUFFICIENT_DATA", "confidence": 0.0,
                "n_connections": n, "gate1": None, "gate2": None}

    icis = np.diff(ts)
    icis_pos = icis[icis > 0]

    # Pre-filter: constant beacon
    if len(icis_pos) >= min_pkts:
        cv = float(np.std(icis_pos) / np.mean(icis_pos)) if np.mean(icis_pos) > 0 else 0
        if cv < 0.05:
            return {"classification": "REGULAR_BEACON", "confidence": 1.0,
                    "n_connections": n, "gate1": None, "gate2": None}

    # ---- Gate 1: Bounded Non-Periodic ----
    g1 = bounded_test(ts)

    if g1["label"] in ("INSUFFICIENT",):
        return {"classification": "INSUFFICIENT_DATA", "confidence": 0.0,
                "n_connections": n, "gate1": g1, "gate2": None}

    if g1["label"] != "BOUNDED":
        if g1["label"] == "NEAR_CONSTANT":
            return {"classification": "JITTERED_BEACON", "confidence": 0.80,
                    "n_connections": n, "gate1": g1, "gate2": None}
        if g1["label"] == "TRENDING":
            return {"classification": "BACKGROUND", "confidence": 1.0,
                    "n_connections": n, "gate1": g1, "gate2": None}
        return {"classification": "BACKGROUND", "confidence": 1.0,
                "n_connections": n, "gate1": g1, "gate2": None}

    # ---- Gate 2: Three-Gap + Discrepancy ----
    g2 = three_gap_test(ts)

    if g2["label"] == "THREE_GAP":
        conf = float((g1["beacon_score"] + g2["beacon_score"]) / 2.0)
        return {"classification": "ROTATION_BEACON", "confidence": round(conf, 3),
                "n_connections": n, "gate1": g1, "gate2": g2}

    return {"classification": "BACKGROUND", "confidence": 1.0,
            "n_connections": n, "gate1": g1, "gate2": g2}


# ============================================================
# Rotation sequence generators (for validation)
# ============================================================

def golden_rotation_times(n, ici_min=30.0, ici_max=120.0, jitter=0.0,
                          seed=42, start=1):
    """Generate intervals using golden ratio rotation."""
    rng = np.random.default_rng(seed)
    fracs = np.array([(i * PHI) % 1.0 for i in range(start, start + n)])
    ivs = ici_min + (ici_max - ici_min) * fracs
    if jitter > 0:
        ivs *= (1 + rng.uniform(-jitter, jitter, n))
        ivs = np.maximum(ivs, ici_min * 0.5)
    return list(np.concatenate([[0.0], np.cumsum(ivs)]))


def sqrt2_rotation_times(n, ici_min=30.0, ici_max=120.0):
    """Generate intervals using sqrt(2) rotation."""
    fracs = np.array([(i * np.sqrt(2)) % 1.0 for i in range(1, n + 1)])
    ivs = ici_min + (ici_max - ici_min) * fracs
    return list(np.concatenate([[0.0], np.cumsum(ivs)]))


def pi_rotation_times(n, ici_min=30.0, ici_max=120.0):
    """Generate intervals using pi rotation."""
    fracs = np.array([(i * np.pi) % 1.0 for i in range(1, n + 1)])
    ivs = ici_min + (ici_max - ici_min) * fracs
    return list(np.concatenate([[0.0], np.cumsum(ivs)]))
